package br.com.code.Interface;

public interface EnumValue {
	/**
	 * Metodo que retorna o valor do Enum declarado.
	 * 
	 * @return String - Valor do Enum
	 */
	public abstract java.lang.String getValue();

	/**
	 * Metodo que retorna a descricao do Enum declarado.
	 * 
	 * @return String - Descricao do Enum
	 */
	public abstract java.lang.String getDescription();
}
